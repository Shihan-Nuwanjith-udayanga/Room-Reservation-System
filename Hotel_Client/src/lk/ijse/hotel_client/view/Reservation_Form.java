    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        try {
            int rid = Integer.parseInt(lblReservationId.getText());
            int cid = Integer.parseInt(txtCustomerId.getText());
            String name = txtCustomerName.getText();
            String address = txtAddress.getText();
            int contact = Integer.parseInt(txtContact.getText());
            int noofcustomer = Integer.parseInt(txtNo_of_customer.getText());
            int mid = Integer.parseInt(txtMealId.getText());
            String meal = comboMeal.getSelectedItem().toString();
            int qty = Integer.parseInt(txtMealQuantity.getText());
            int roid = Integer.parseInt(txtNumberOfRooms.getText());
            String room_type = (String) comboRoom.getSelectedItem();
            int rqty = Integer.parseInt(txtSelectedRooms.getText());
            String arrivalDate = txtArrivalDate.getText();
            String depatureDate = txtDepatureDate.getText();
            
             ReservationDTO reservationDTO = new ReservationDTO(
                     rid, cid, name, address, contact, noofcustomer,mid,meal,qty,roid,room_type,rqty,arrivalDate,depatureDate);
            boolean book = controller.book(reservationDTO);            
            if (book) {
                JOptionPane.showMessageDialog(this, "Booking Completed...");
                setClear();
                setID();
            }else{
                JOptionPane.showMessageDialog(this, "Booking Fail...");
            }
        } catch (Exception ex) {
            Logger.getLogger(Reservation_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBookActionPerformed

