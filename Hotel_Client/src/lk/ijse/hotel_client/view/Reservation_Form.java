    private void comboRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboRoomActionPerformed
        try {
            String type = comboRoom.getSelectedItem().toString();
            ArrayList<ReservationDTO> roomDetails = controller.searchByType(type);
            model = (DefaultTableModel) tblResevation.getModel();
                model.setRowCount(0);
            for (ReservationDTO roomDetail : roomDetails) {
                Object[] ob = {roomDetail.getReservation_id(),
                    roomDetail.getRid(),
                    roomDetail.getRoom_type(),
                    roomDetail.getDeparture_date()};
                model.addRow(ob);
            }
        } catch (Exception ex) {
            Logger.getLogger(Reservation_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_comboRoomActionPerformed

